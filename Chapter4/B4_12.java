public class B4_12 {
    public static void main(String[] args) {
        char c = 'A';
        int i = (int)c;
        System.out.println(i);

        float f = 1000.34f;
        int j = (int)f;
        System.out.println(j);

        double d = 1000.34;
        int k = (int)d;
        System.out.println(k);

        int l = 97;
        char p = (char)l;
        System.out.println(p);
    }
}
